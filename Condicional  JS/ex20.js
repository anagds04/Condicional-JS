let opção = "2";

switch (opção) {

    case "1":
        console.log("Pagamento via Pix");
        break;

    case "2":
        console.log("Pagamento via crédito");
        break;


    case "3":
        console.log("Pagamento via débito");
        break;

    case "4":
        console.log("Pagamento em dinheiro");
        break;

    default:
        console.log("Pagamento inexistente.")
}